/**
 * JavaScript 이벤트 루프: 비동기 API는 작업을 예약하고 바로 반환한다.
 *
 * setTimeout은 콜백을 나중에 실행하도록 예약하고 즉시 다음 코드로 넘어간다.
 */

export {}

setTimeout(() => console.info('A: timeout 1ms'), 1)
setTimeout(() => console.info('B: timeout 2ms'), 2)
console.info('C: synchronous')

// 출력 순서:
// C: synchronous
// A: timeout 1ms
// B: timeout 2ms
//
// 실행 순서:
// 1. 첫 번째 setTimeout(...) 호출
//    - 콜백 참조와 값1을 아규먼트로 하여 네이티브 타임아웃 API를 호출한다.
// 2. 두 번째 setTimeout(...) 호출
//    - 콜백 참조와 값2를 아규먼트로 하여 네이티브 타임아웃 API를 호출한다.
// 3. console.info('C...')는 동기 코드이므로 즉시 실행된다.
//    - 그래서 C가 가장 먼저 출력된다.
// 4. 백그라운드에서 1밀리초가 지나면, 네이티브 타임아웃 API는 이벤트 루프에 콜백을 넣는다.
// 5. 다시 1밀리초가 지난 다음에, 네이티브 타임아웃 API는 이벤트 루프에 두 번째 콜백을 넣는다.
// 6. 3번 과정을 완료한 후 콜스택이 비었으므로 이벤트 큐에 태스크가 있는지 확인한다.
//    4, 5번 과정 중 하나라도 완료되었다면 이벤트 큐에 태스크가 존재할 것이다.
//    이벤트 큐에서 태스크를 꺼내 관련 콜백 함수를 호출한다.
// 7. 설정한 두 타임아웃이 모두 지났고, 이벤트 큐와 콜 스택이 모두 비었다면 프로그램이 종료된다.